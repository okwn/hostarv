document.addEventListener('DOMContentLoaded', () => {

    // Intersection Observer for Scroll Animations
    const observerOptions = {
        root: null,
        rootMargin: '0px',
        threshold: 0.1
    };

    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach((entry, index) => {
            if (entry.isIntersecting) {
                setTimeout(() => {
                    entry.target.classList.add('visible');
                }, index * 100);
                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);

    const cards = document.querySelectorAll('.card');
    cards.forEach(card => {
        observer.observe(card);
    });

    // Language Handling
    const langDropdown = document.querySelector('.language-dropdown');
    const langBtn = document.querySelector('.lang-btn');
    const langItems = document.querySelectorAll('.lang-item');
    const currentFlag = langBtn.querySelector('.flag');
    const currentCode = langBtn.querySelector('.lang-code');
    const currentLang = document.documentElement.lang || 'en';

    // Toggle dropdown
    langBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        langDropdown.classList.toggle('active');
    });

    // Close when clicking outside
    document.addEventListener('click', (e) => {
        if (!langDropdown.contains(e.target)) {
            langDropdown.classList.remove('active');
        }
    });

    // Handle Language Selection
    langItems.forEach(item => {
        item.addEventListener('click', (e) => {
            e.preventDefault();
            const lang = item.dataset.lang;
            const flag = item.querySelector('.flag').textContent;

            // Update Button UI
            currentFlag.textContent = flag;
            currentCode.textContent = lang.toUpperCase();

            // Update Active State
            langItems.forEach(i => i.classList.remove('active'));
            item.classList.add('active');

            // Apply Translation
            changeLanguage(lang);

            // Close Dropdown
            langDropdown.classList.remove('active');
        });
    });

    function changeLanguage(lang) {
        // Fallback to English if translation completely missing (unlikely now)
        const dict = translations[lang] || translations['en'];

        // Loop through all elements with data-i18n
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.dataset.i18n;
            if (dict[key]) {
                el.innerHTML = dict[key];
            } else if (translations['en'][key]) {
                // Determine if we should show English for missing keys
                el.innerHTML = translations['en'][key];
            }
        });

        // Update basic page attributes
        document.documentElement.lang = lang;
    }

    // Hamburger Interaction
    const hamburger = document.querySelector('.hamburger');
    hamburger.addEventListener('click', () => {
        hamburger.style.transform = 'scale(0.95)';
        setTimeout(() => hamburger.style.transform = 'scale(1)', 150);
    });

    // Header scroll background effect
    const header = document.querySelector('.header');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            header.style.background = 'rgba(13, 14, 18, 0.9)';
            header.style.boxShadow = '0 1px 0 rgba(255,255,255,0.05)';
        } else {
            header.style.background = 'rgba(13, 14, 18, 0.7)';
            header.style.boxShadow = 'none';
        }
    });
});
